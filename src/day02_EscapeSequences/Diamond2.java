package day02_EscapeSequences;

public class Diamond2 {
    public static void main(String[] args) {
        System.out.println("    ^  ");
        System.out.println("   /  \\  ");
        System.out.println("  /  * \\  ");
        System.out.println(" / *   *\\  ");
        System.out.println("/*   *  *\\  ");
        System.out.println("__________ ");
        System.out.println("\\        /  ");
        System.out.println(" \\      /  ");
        System.out.println("  \\    /  ");
        System.out.println("   \\  /  ");
        System.out.println("    V");
    }
}
